      <h4>SONIC GATES: OKC, 2015</h4>

      <p>
        Christopher Janney <br/>
        Architect/Artist/Composer
      </p>

      <p>
        Located at WRWA Car Rental Center, 5201 S. Meridian Ave. (open to the public)
      </p>

      <p>
        Funded by the Oklahoma City Airport Trust 1% for art
      </p>

      <p>
        Sonic Gates: OKC is integrated into the main entrance of the Car Rental Center, immersing those who pass through
        in an interactive light and sound experience. This is a singular type of permanent public art work yet to be
        experienced in the Oklahoma City area.
      </p>

      <p>
        Janney was trained as an architect and as a jazz musician. As an artist he brings the two disciplines together
        to create unexpected spaces that depend on the viewer’s interaction. For Sonic Gates: OKC, it is the movement of
        the people walking up to the building, the sliding doors opening for them, and then walking through the
        vestibule, that creates the unique experience.
      </p>

      <p>
        The panes of glass that make up the sliding doors have different interlayers of color. As the doors open and
        close, they layer upon themselves, creating new fields of color. The colors are always changing depending on how
        many people and at what moment they walk through the vestibule.
      </p>

      <p>
        At the same time, the soundscapes and the lights are complimenting the colored environment, layering and
        creating an entirely original event. The soundscapes, original compositions by Janney, consists of instrumental
        music combined with specific sounds and short readings that evoke interesting aspects of Oklahoma. Janney’s
        research included an interview with the Executive Director of the Oklahoma Historical Society and delving into
        the audio archives at the Oklahoma History Center.
      </p>

      <p>
        “My goal is always to create work that engages people - physically, mentally and emotionally,” said Janney at
        the art opening for Sonic Gates: OKC. “As people walk up to this building and through the vestibule, they will
        be bathed in colored shadows, activate lights and trigger authentic Oklahoma-centric soundscapes. For people
        traveling to and from Oklahoma City, this work helps to center where they are in the world.”
      </p>
