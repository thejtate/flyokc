<p>
  The Will Rogers World Airport (WRWA) Operations Division is responsible for issuing airport identification
  badges to
  employees who work at the airport and have access to restricted or security sensitive areas. We issue badges to
  airline employees, concessionaires, tenants, vendors, contractors, consultants, government employees and the
  Department of Airports employees.
</p>

<h4>BADGING OFFICE HOURS</h4>

<p>
  Badging office hours are as follows:
</p>

<p class="rteindent1">
  Monday – Friday <br/>
  9:00am to 11:00am <br/>
  1:00pm to 3:00pm
</p>

<p>
  The office is closed for the following holidays: New Year’s Day, Martin Luther King Day, Memorial Day,
  Independence Day,
  Labor Day, Veteran’s Day, Thanksgiving (Thursday and Friday), Christmas Eve and Christmas Day.
</p>

<p>
  The Badging Office is located in the basement of the WRWA terminal building. Applicants should park in the
  hourly
  parking lot. Take the elevator to the tunnel level (press “T”.) Proceed toward the terminal building. When you
  approach
  the escalators going up to the baggage claim, walk around to the back side of the escalators. Follow the sign to
  the
  badging office which will be through the doors on the left. Click here to see a map.
</p>

<h4>HOW TO APPLY</h4>

<p>
  All badge applicants must complete the required documentation, pass the necessary background checks and
  successfully complete training. All the forms needed for the badging process are individually linked in the
  steps
  below and are fillable on your computer. The forms cannot be saved, so if you do not finish filling out a form
  before you close it out, you will need to start again when you reopen the form. Just follow the steps below or
  print this checklist.
</p>

<p>
  If you are unsure about what forms you need to complete, your employer can guide you. Once completed, print the
  forms, and return to your employer for approval and further instructions.
</p>

<p>
  <strong>For a New, Renewal, Expired, Lost, Stolen or Destroyed Badge</strong>
</p>

<p>
  Important Notes: For all lost, stolen or destroyed badges, immediately report them by calling Airport Police at
  405-297-1184. Also, add step 6 to the badge application process below. There are penalties and fees for
  replacement of lost, stolen, destroyed or expired badges.
</p>

<p>
  1. Complete the Security Badge Application.
</p>

<p class="rteindent1">
  All applicants must fill out Section 1 of the “Security Badge Application Form.” If you are an employee who will
  have access to security sensitive areas, you must complete both Sections 1 and 2. Once you have filled out the
  form, print the form double-sided. This document will not be accepted if it is not printed double-sided. You will
  bring this form to the employer’s designated Authorized Signatory for signature, and then you will bring it to the
  Badging Office.
</p>

<p>
  AIRLINES ONLY: For new employees, please fill out the Aircraft Operator Fingerprint Request
</p>

<p>
  2. Complete the Acceptable ID Documents List.
</p>

<p class="rteindent1">
  You will bring the same documents with you that are checked on this form to the employer’s designated Authorized
  Signatory for their signature and then to the Badging Office.
</p>

<p>
  3. Complete and sign the Notice of Criminal History Records Check.
</p>

<p class="rteindent1">
  Once you have filled out the form, print the form double-sided. This document will not be accepted if it is not
  printed double-sided. Sign and date both the front and the back of the form and bring to the employer’s designated
  Authorized Signatory for signature and then to the Badging Office. If you have a Disqualifying Crime, you will
  need to speak to the employer’s designated Authorized Signatory.
</p>

<p>
  4. Complete and sign the Notice of Privacy Act and Certification Statements.
</p>

<p class="rteindent1">
  You will bring this form to the Badging Office.
</p>

<p>
  5. If your job requires you to drive equipment or vehicles on any part of the airport where aircraft operations
  occur or in restricted areas, complete and sign the Driver’s License Verification for Operating on the AOA of
  WRWA.
</p>

<p class="rteindent1">
  You will bring this form to the Badging Office at which time you will be scheduled for driver training.
</p>

<p>
  6. For all lost, stolen or destroyed badges, complete and sign the Lost Security Badge form.
</p>

<p class="rteindent1">
  You will bring this form to the Badging Office, along with the required items above.
</p>

<p>
  Please double-check that you have completed all the applicable documents. All forms must first be brought to
  your
  employer’s designated Authorized Signatory for review and signatures and then to the badging office. Do not fold
  any of the documents.
</p>

<p>
  Once all forms have been completed and signed by your employer’s designated Authorized Signatory, you can come
  to
  the badging office to begin the badging process.
</p>

<h4>FEE INFORMATION</h4>

<p>
  Badge $15 initial; $10 renewal <br/>
  Fingerprinting $35 <br/>
  Replacement $25 first; $50 second; $100 third and subsequent
</p>

<h4>RETURNING BADGES</h4>

<p>
  Please use this Badge Receipt Form when returning multiple badges. Be sure to supply all of the information
  requested on the form. Remember to include your company’s name, specific badge details, your name and the total
  number of badges. Print two (2) copies of the completed form and deliver them, with the badges, to the badging
  office. You do not have to wait in line, staff will quickly check the information and sign one copy of the
  receipt for your records. Please let us know if you have any questions about the Badge Receipt Form.
</p>

<h4>FAQS</h4>

<p>
  <strong>Q: Do I need an appointment to come to the Badging Office?</strong> <br/>
  A: No.
</p>

<p>
  <strong>Q: Do you accept credit cards? </strong><br/>
  A: All payments are made to the receptionist in Airport Administration. This office is located on the third
  floor of the terminal building and is open from 8:00 a.m. until 4:30 p.m. Acceptable forms of payment are cash,
  check and Visa/MasterCard.
</p>


<p>
  <strong>Q: Do you validate parking? </strong><br/>
  A: Not at this time.
</p>

<p>
  <strong>Q: How do I get a badge? </strong><br/>
  A: The process for getting a badge is outlined under the “How To Apply” section of this page.
</p>

<p>
  <strong> Q: How much does a badge cost?</strong> <br/>
  Badge $15 initial; $10 renewal <br/>
  Fingerprinting $35 <br/>
  Replacement $25 first; $50 second; $100 third and subsequent
</p>

<p>
  <strong>Q: Will my badge have access right away?</strong> <br/>
  A: Once your company’s Authorized Signatory has signed all the appropriate forms and you have completed all
  required training, your badge will be activated.
</p>

<p>
  <strong>Q: When does my badge expire? </strong><br/>
  A: There is a date at the bottom of your badge. This is the expiration date. Badges must be renewed at the
  badging office or returned upon expiration.
</p>

<p>
  <strong>Q: Do I only have to take training one time? </strong><br/>
  A: This is dependent on the type(s) of training required for your job activities.
</p>

<p>
  <strong>Q: Can ID badges be mailed to the employee or company?</strong> <br/>
  A: For security reasons, all ID badges must be picked up in person by the original ID badge applicant.
</p>

<p>
  <strong>Q: How will I know when my badge is ready? </strong><br/>
  A: Your company will be notified via e-mail when your background checks are cleared. Badges must be picked up
  within 30 days of this notification. Valid identification must be presented when you pick up your badge.
</p>

<p>
  <strong>Q: I committed a crime, but it wasn’t one that will prevent me from getting a badge. What paperwork do you
    need
    from me? </strong><br/>
  A: All fingerprint results with records will be reviewed by the Airport Security Coordinator's office. They will
  determine if your crime was a disqualifying crime and will advise you in writing of the additional requirements
  (if any).
</p>
