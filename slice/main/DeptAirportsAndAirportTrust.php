        <h4>Department of Airports</h4>

        <p>
          The Oklahoma City Department of Airports is comprised of three airports: Will Rogers World Airport, Wiley Post
          Airport and Clarence E. Page Airport. The airports are owned by the City of Oklahoma City and leased to the
          Oklahoma City Airport Trust, which oversees the management of the facilities. The department is led by Mark
          Kranenburg, Director of Airports, who is responsible for the operations of the airport system.
        </p>

        <p>
          WILL ROGERS WORLD AIRPORT
        </p>

        <p>
          With three major runways—two 9,800-ft parallel runways and one 7,800-ft crosswind runway—Will Rogers World
          Airport can accommodate any size aircraft. With a strong mix of commercial, freight, military, corporate and
          general aviation activity, the number of operations for 2014 neared 123,000. Almost 118,000 operations were
          recorded in 2015.
          Six commercial carriers serve Will Rogers, with an average of 75 total daily departures. Total passenger
          traffic for 2014 was 3,834,009, an all-time record for WRWA. Total passenger traffic for 2015 was 3,721,525.
        </p>

        <p>
          The airport is home to 70 companies that employ nearly 12,000 people. Major tenants include the FAA's Mike
          Monroney Aeronautical Center, AAR Aircraft Services – Oklahoma, Field Aerospace, Atlantic Aviation, the
          Federal Bureau of Prisons Transfer Center, the U.S. Customs & Border Protection National Air Training Center,
          Southwest Airlines Reservation Center and the Metro Tech Aviation Career Center.
        </p>

        <p>
          Will Rogers World Airport encompasses Oklahoma City's Foreign Trade Zone, providing cargo logistics and
          tariff-related advantages for companies who trade internationally. With its central location just minutes from
          “America’s Crossroads” (the junction of I-35, I-40 and I-44) and with large developable tracts of land that
          include property adjacent to I-44 known as Lariat Landing, Will Rogers World Airport offers great development
          opportunities for aviation, aviation support, industrial, office and retail businesses.
        </p>
